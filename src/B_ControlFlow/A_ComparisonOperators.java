package B_ControlFlow;

public class A_ComparisonOperators {
    public static void main(String[] args) {
        int x = 1;
        int y = 1;

        System.out.println(x == y); // two equal sign reads as "equal to"
        System.out.println(x != y); // not equal to
        // < less than
        // > great than
        // <= less than or equal to
        // >= greater than or equal to

    }
}
