package B_ControlFlow;

public class G_ForLoops {

    public static void main(String[] args) {
        // Read as
        // Set variable i to 0
        // "For" every time that i is less than 5 continue
        // increase i value by 1
        for (int i = 0; i < 5; i++) {
            System.out.println("Hello World " + i);
        }
    }
}
