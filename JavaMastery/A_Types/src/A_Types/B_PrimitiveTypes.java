package A_Types;
public class B_PrimitiveTypes {
    public static void main(String[] args) {
        byte age = 30;
        // Underscore acts like a comma to make long numbers readable
        // When using long use "L" at the end
        long viewsCount = 123_456_789L;
        double price = 10.99;
        float anotherPrice = 11.99F;
        // Char uses single quote
        // String use double quotes
        char letter = 'A';
        boolean isEligible = false;
    }
}
